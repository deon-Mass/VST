package cd.digitalEdge.vst.Objects;

public class Sellings {
    public String id            = "id";
    public String selling_date  = "selling_date";
    public String total_amount  = "total_amount";
    public String vendor_id     = "vendor_id";
    public String customer_id   = "customer_id";
    public String created_by    = "created_by";
    public String created_at    = "created_at";
    public String updating      = "updating";
    public String status        = "status";

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSelling_date() {
        return selling_date;
    }

    public void setSelling_date(String selling_date) {
        this.selling_date = selling_date;
    }

    public String getTotal_amount() {
        return total_amount;
    }

    public void setTotal_amount(String total_amount) {
        this.total_amount = total_amount;
    }

    public String getVendor_id() {
        return vendor_id;
    }

    public void setVendor_id(String vendor_id) {
        this.vendor_id = vendor_id;
    }

    public String getCustomer_id() {
        return customer_id;
    }

    public void setCustomer_id(String customer_id) {
        this.customer_id = customer_id;
    }

    public String getCreated_by() {
        return created_by;
    }

    public void setCreated_by(String created_by) {
        this.created_by = created_by;
    }

    public String getCreated_at() {
        return created_at;
    }

    public void setCreated_at(String created_at) {
        this.created_at = created_at;
    }

    public String getUpdating() {
        return updating;
    }

    public void setUpdating(String updating) {
        this.updating = updating;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }



}
